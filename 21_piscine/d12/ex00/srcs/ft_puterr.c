#include "../includes/ft.h"

int		ft_puterr(int n)
{
	if (n == 0)
		write(2, NO_FILE, 19);
	else if (n == 1)
		write(2, INV_ARGS, 20);
	else if (n == 2)
		write(2, INV_FILE, 18);
	return (1);
}
