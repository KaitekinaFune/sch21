#include "../includes/ft.h"

void	ft_inf_input(void)
{
	char	buf[BUF_SIZE];
	int		ret;

	while (1 > 0)
	{
		ret = read(2, buf, BUF_SIZE);
		write(1, buf, ret);
	}
}
