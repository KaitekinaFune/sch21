#include "../includes/ft.h"

int		ft_error(int n)
{
	if (n == 0)
	{		
	write(2, "0\n", 2);
		return (1);
	}
	write(2, "Stop : ", 7);
	if (n == 1)
		write(2, "division by zero\n", 17);
	else if (n == 2)
		write(2, "modulo by zero\n", 15);
	return (1);
}
