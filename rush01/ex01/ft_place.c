#include <stdlib.h>

char	*ft_place(char *str, int i)
{
	char *c;
	int index;
	int j;

	c = malloc(4 * sizeof(char));
	index = (((i - 1) * 8) + 2);
	j = 0;
	while (j <= 6)
		c[j++] = str[index++];
	c[j] = '\0';
	return (c);
}
